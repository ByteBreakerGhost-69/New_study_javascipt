// pengondisian ( if ... else )
// untuk memberikan intruksi agar program dapat berfikir
let nilai = 80;
if (nilai >= 75) {
    console.log("Lulus!");
} else {
    console.log("Coba lagi.");
}

// pengulangan ( for loop )
// untuk menjalankan code berkali kali tanpa mengulang
for (let i = 1; i <= 5; i++) {
    console.log("Baris ke-" + i);
}
